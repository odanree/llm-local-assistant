const str = '{PROJECT_ROOT}/utils.ts';
console.log('1. Input:', str);
let clean = str.trim();
console.log('2. After trim:', clean);
clean = clean.replace(/[`]/g, '');
console.log('3. After backticks:', clean);
clean = clean.replace(/\.{2,}$/, '');
console.log('4. After dots:', clean);
clean = clean.replace(/[\u0000-\u001F\u007F-\u009F]/g, '');
console.log('5. After control chars:', clean);
clean = clean.replace(/[*_~]/g, '');
console.log('6. After markdown:', clean);
clean = clean.replace(/[,;!?:]+$/, '');
console.log('7. After trailing punct:', clean);
clean = clean.replace(/["']/g, '');
console.log('8. After quotes:', clean);
clean = clean.replace(/\s+/g, ' ').trim();
console.log('9. After whitespace:', clean);
console.log('FINAL:', clean);
